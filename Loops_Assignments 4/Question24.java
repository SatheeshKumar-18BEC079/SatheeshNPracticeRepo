package Loops_Assignments4;

public class Question24 {

	public static void main(String[] args) {
            int a[]= {5,4,3,2,1};
		
		
		for(int i=0;i<a.length;i++)
		{
				
			for(int j=i;j<a.length;j++)
			{
				
				System.out.print(a[j]+" ");
				
			}
			System.out.println();
		}

	}

}
